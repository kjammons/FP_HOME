      <h2>Introduction: The Social Construction of Whiteness</h2>

      <p>
        Housing discrimination remains one of the most powerful tools for creating and maintaining racial inequality in the United States by design. Through mechanisms like restrictive covenants, redlining, and exclusionary zoning, access to land and wealth has been systematically reserved for those deemed “white.” Beneath these policies lies a deeper story: “whiteness” itself — far from being a natural or biological category — is a socially constructed tool meant to consolidate power, privilege, and belonging.
      </p>

      <p>
        Whiteness as a tool of possession and dispossession is both socially constructed and dynamic. It is a definition that demonstrably evolved to confer privilege, restrict opportunity, and preserve social hierarchies. This process has been enforced and maintained through the full apparatus of American life: law, housing, education, labor, and cultural practice.
      </p>

      <p>
        In Massachusetts, new insights from public property deeds, scanned and examined in the aggregate to identify trends, ground a social construction of whiteness in local form. Middlesex County, with proliferating white suburban developments in the nineteenth and twentieth centuries, offers a case study in how racialized landscapes were deliberately manufactured through property law and decentralized neighborhood action. This project, and its three educational modules, tell the story of one understudied tool in constructing regimes of white supremacy in housing: restrictive covenants.
      </p>

      <p>
        Restrictive covenants are clauses that were inserted into property deeds to prevent people who were not white from buying or occupying land (<em>Mapping Prejudice, University of Minnesota</em>). Restrictive covenants proliferated separately from government-sponsored means of discrimination like redlining or exclusionary zoning, embodying a populist, insidious desire of neighbors to exclude fellow neighbors. Private actors — such as developers, homeowners' associations, real estate boards, and individual property owners — wielded covenants to the ends of racial and ethnic segregation. These covenants still “run with the land,” alive in property deeds although now ruled unenforceable and illegal following a series of Supreme Court cases and national legislation: <em>Hansberry v. Lee</em> (1940), <em>Shelley v. Kraemer</em> (1948), and the <em>Fair Housing Act</em> (1968).
      </p>

      <p>
        Our goal in highlighting these covenants is to reveal their role in racialized exclusion. Additionally, we confront the historical processes by which socially constructed whiteness was continually reasserted and maintained: through neighborhood control, economic gatekeeping, and the manipulation of law.
      </p>

      <p>
        This project is inspired by and developed in accordance with the long-term goals of the Massachusetts Covenants Project. Led by researchers at MIT’s Department of Urban Studies and Planning, the project seeks to identify and analyze restrictive covenants embedded in property deeds received from Northern Middlesex County Registry of Deeds Office. By uncovering and documenting the hidden histories of restrictive covenants, the project illuminates the tools by which racial inequality was and remains written into the landscapes we inhabit.
      </p>

      <p>
        This public educational series on restrictive covenants guides users through modules designed to:
      </p>

      <ul>
        <li>Introduce a “no previous knowledge” user to restrictive covenants as a tool and concept, with a focus on Middlesex County residents.</li>
        <li>Zoom out from an articulation of white supremacist housing regimes in the county to a narrative timeline of housing discrimination in the United States, supplemented by demographic and homeownership data for Middlesex.</li>
        <li>Provide users with the agency to explore particular municipalities’ contemporary demographic and homeownership data, as pulled for 2023.</li>
      </ul>

      <p>
        The modules are designed to serve different purposes and can be deployed or interacted with alone or as part of the series. The authors have provided a suggested “workflow” for the content, but purposefully have granted users the agency to follow or diverge from this order.
      </p>

